function add(x,y){
return x+y
}

function subtract(x,y){
return x-y
}

function multiply(x,y){
return x*y
}

function divide(x,y){
return x/y
}

function increment(x){
return x+1
}

function decrement(x){
return x-1
}

function makeInt(a){
return parseInt(a,10)
}

function preserveDecimal(x){
return parseFloat(x,2)
}

